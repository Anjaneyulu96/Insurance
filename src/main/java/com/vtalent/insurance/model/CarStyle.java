package com.vtalent.insurance.model;

public class CarStyle {
	private int id;
	private int y_id;
	private int c_id;
	private String name;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getY_id() {
		return y_id;
	}

	public void setY_id(int y_id) {
		this.y_id = y_id;
	}

	public int getC_id() {
		return c_id;
	}

	public void setC_id(int c_id) {
		this.c_id = c_id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
